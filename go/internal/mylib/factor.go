func MinFactor(n int) int {
	if n <= 0 {
		panic("argument must be a positive number")
	}

	if n&1 == 0 {
		return 2
	}
	if n <= maxUINT16 {
		if factor := minFactorTbl[n>>1]; factor == 1 {
			return n
		} else {
			return factor
		}
	}

	if n%3 == 0 {
		return 3
	}
	for i := 5; ; i += 6 {
		if n%i == 0 {
			return i
		}
		if n%(i+2) == 0 {
			return i + 2
		}
	}
}

func PrimeFactorization(n int) []int {
	if n < 1 {
		panic("invalid argument")
	}
	if n == 1 {
		return []int{1}
	}

	result := make([]int, 0)
	for _, b := range []int{2, 3, 5} {
		e := 0
		for n%b == 0 {
			e++
			n /= b
			result = append(result, b)
		}
	}

	diff := []int{4, 2, 4, 2, 4, 6, 2, 6}
	b := 7
	idx := 0
	limit := Isqrt(n)

	for b <= limit && n != 1 {
		e := 0
		for n%b == 0 {
			e++
			n /= b
			result = append(result, b)
		}
		b += diff[idx]
		idx = (idx + 1) & 0b111
	}
	if n != 1 {
		result = append(result, n)
	}

	return result
}

func PfactorsToDivisors(lst []int) []int {
	if len(lst) == 1 && lst[0] == 1 {
		return []int{1}
	}

	m1 := make(map[int]struct{})
	m1[1] = struct{}{}

	for x := range slices.Values(lst) {
		m2 := make(map[int]struct{})
		for y := range maps.Keys(m1) {
			m2[x*y] = struct{}{}
		}
		maps.Copy(m1, m2)
	}
	result := slices.Collect(maps.Keys(m1))
	slices.Sort(result)

	return result
}

func Divisors(n int) []int {
	return PfactorsToDivisors(PrimeFactorization(n))
}
