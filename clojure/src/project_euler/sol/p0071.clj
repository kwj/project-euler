(ns project-euler.sol.p0071)

;;;;  Farey sequence
;;;;
;;;;   2/5, 3/7
;;;;     -> 2/5, (2+3)/(5+7), 3/7
;;;;     -> 2/5, (2+3)/(5+7), (2+3+3)/(5+7+7), 3/7
;;;;     -> 2/5, (2+3)/(5+7), (2+3+3)/(5+7+7), (2+3+3+3)/(5+7+7+7), 3/7
;;;;      ...
;;;;     -> 2/5, ..., (2+3x)/(5+7x), 3/7
;;;;
;;;;       5+7x <= 1_000_000

(defn solve
  ([]
   (solve 1000000))
  ([limit]
   (+ 2 (* 3 (quot (- limit 5) 7)))))
